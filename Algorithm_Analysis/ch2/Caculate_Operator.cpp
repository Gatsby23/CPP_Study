//----------使用指针变量的情况下,完成常规的实数计算
#include <iostream>
using namespace std;

void _Add(){
	double *_number1 = new double;
	double *_number2 = new double;
	cin >> *_number1 >> *_number2;
	cout << *_number1 << " + " << *_number2;
	cout << " = " <<  (*_number1 + *_number2) << endl;;
}

int main()
{
	_Add();
	return 0;
}
